HANGUL_START = ord("가")
HANGUL_END = ord("힣")


@dataclass(frozen=True)
class HangulSyllable:
    syllable: str
    initial: str
    vowel: str
    final: str


def split_syllable(char: str) -> HangulSyllable | None:
    code = ord(char)

    if code < HANGUL_START or code > HANGUL_END:
        return None

    index = code - HANGUL_START
    initial_index = index // 588
    vowel_index = (index % 588) // 28
    final_index = index % 28

    return HangulSyllable(
        syllable=char,
        initial=INITIALS[initial_index],
        vowel=VOWELS[vowel_index],
        final=FINALS[final_index],
    )
